class Person
{
  Integer kraft;
  Integer iq;
  
  Person(int kraft, int iq) {
    this.kraft = kraft;
    this.iq = iq;
  }
  
  void setKraft(int kraft) {
    this.kraft = kraft;  
  }
  
  int getKraft() {
    return this.kraft;  
  }
  
  void setIq(int iq) {
    this.iq=iq;  
  }
  
  int getIq() {
    return this.iq;
  }
}
